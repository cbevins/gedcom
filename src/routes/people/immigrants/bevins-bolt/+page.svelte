        <div class="topic">Emigration</div>
        <p>
            William Longford Bevins, his wife Mary Bolt, and sons John, Joseph,
            and William Alfred emigrated from Liverpool, England,
            arriving in New York on August 21, 1854.
        </p>
        <p>
        They sailed on the Collins Line ship <class id="shipname">Baltic</class>
        (2856 tons, 3 masts, 1 funnel).
        </p>
        <hr>
        <div class="topic">Migration</div>
        <p>
            Within a year of arriving in New York, the Bevins-Bolt family had removed to
            Newark NJ for the birth of Sarah Anne in Aug 9, 1845.
        </p>
        <p>
            The 1850 US Census places the family as farmers in Townsend, Huron, OH.    
            Sarah Anne's obituary says the family stayed in Ohio until she was 10 (1855),
            then removed again to Henry Co, IL,
            while her 50th anniversary article says she moved to Henry Co, IL in 1858.
        </p>
        <p>
            The 1860, 1870, 1880, and 1890 US Census all place William Longford Bevins
            as a farmer in Annawan, Henry, IL 
        </p>
