                    The first mention of Newbold Verdon is in the Domesday Book (1086),
                or "Great Survey", commissioned by William the Conquerer to inventory
                his newly won kingdom.
                It was originally an agricultural center that grew in size in the 1850s
                with the expansion of coal mining in the area.
                </p>
                <p>
                    Is it possible that farmers like the Bevins sold their land
                    or had it siezed for mining?  Perhaps more likely,
                    after several generations of large families,
                    there was little land left to divvy up among the sons
                    and the latter-born had to look elsewhere.
                    They could have stayed in Leicestershire and become miners,
                    but instead chose to remain farmers by emigrating to the US.
                </p>
